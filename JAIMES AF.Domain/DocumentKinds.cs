namespace MattEland.Jaimes.Domain;

/// <summary>
/// Constants for document kind values used throughout the application.
/// </summary>
public static class DocumentKinds
{
    /// <summary>
    /// Represents a sourcebook document (e.g., Player's Handbook, Dungeon Master's Guide).
    /// </summary>
    public const string Sourcebook = "Sourcebook";

    /// <summary>
    /// Represents an adventure module document.
    /// </summary>
    public const string Adventure = "Adventure";

    /// <summary>
    /// Represents a transcript document.
    /// </summary>
    public const string Transcript = "Transcript";
}

